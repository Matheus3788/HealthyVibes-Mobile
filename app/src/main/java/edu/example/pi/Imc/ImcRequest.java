package edu.example.pi.Imc;

public class ImcRequest {
    private double peso;
    private int altura;

    public ImcRequest(double peso, int altura) {
        this.peso = peso;
        this.altura = altura;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
}
